var AR = AR || {};

AR.Marker = function (id, corners) {
	this.id = id;
	this.corners = corners;
};

AR.Detector = function () {
	this.grey = new CV.Image();
	this.thres = new CV.Image();
	this.homography = new CV.Image();
	this.binary = [];
	this.contours = [];
	this.polys = [];
	this.candidates = [];
};

AR.Detector.prototype.detect = function (image) {
	CV.grayscale(image, this.grey);
	CV.adaptiveThreshold(this.grey, this.thres, 2, 3);
	this.contours = CV.findContours(this.thres, this.binary);
	this.candidates = this.findCandidates(
		this.contours,
		image.width * 0.01,
		0.05,
		10,
	);
	this.candidates = this.clockwiseCorners(this.candidates);
	this.candidates = this.notTooNear(this.candidates, 10);
	return this.findMarkers(this.grey, this.candidates, 49);
};

AR.Detector.prototype.findCandidates = function (
	contours,
	minSize,
	epsilon,
	minLength,
) {
	var candidates = [],
		len = contours.length,
		contour,
		poly,
		i;

	this.polys = [];

	for (i = 0; i < len; ++i) {
		contour = contours[i];

		if (contour.length >= minSize) {
			poly = CV.approxPolyDP(contour, contour.length * epsilon);

			this.polys.push(poly);

			if (4 === poly.length && CV.isContourConvex(poly)) {
				if (CV.minEdgeLength(poly) >= minLength) {
					candidates.push(poly);
				}
			}
		}
	}

	return candidates;
};

AR.Detector.prototype.clockwiseCorners = function (candidates) {
	var len = candidates.length,
		dx1,
		dx2,
		dy1,
		dy2,
		swap,
		i;

	for (i = 0; i < len; ++i) {
		dx1 = candidates[i][1].x - candidates[i][0].x;
		dy1 = candidates[i][1].y - candidates[i][0].y;
		dx2 = candidates[i][2].x - candidates[i][0].x;
		dy2 = candidates[i][2].y - candidates[i][0].y;

		if (dx1 * dy2 - dy1 * dx2 < 0) {
			swap = candidates[i][1];
			candidates[i][1] = candidates[i][3];
			candidates[i][3] = swap;
		}
	}

	return candidates;
};

AR.Detector.prototype.notTooNear = function (candidates, minDist) {
	var notTooNear = [],
		len = candidates.length,
		dist,
		dx,
		dy,
		i,
		j,
		k;

	for (i = 0; i < len; ++i) {
		for (j = i + 1; j < len; ++j) {
			dist = 0;

			for (k = 0; k < 4; ++k) {
				dx = candidates[i][k].x - candidates[j][k].x;
				dy = candidates[i][k].y - candidates[j][k].y;

				dist += dx * dx + dy * dy;
			}

			if (dist / 4 < minDist * minDist) {
				if (CV.perimeter(candidates[i]) < CV.perimeter(candidates[j])) {
					candidates[i].tooNear = true;
				} else {
					candidates[j].tooNear = true;
				}
			}
		}
	}

	for (i = 0; i < len; ++i) {
		if (!candidates[i].tooNear) {
			notTooNear.push(candidates[i]);
		}
	}

	return notTooNear;
};

AR.Detector.prototype.findMarkers = function (imageSrc, candidates, warpSize) {
	var markers = [],
		len = candidates.length,
		candidate,
		marker,
		i;

	for (i = 0; i < len; ++i) {
		candidate = candidates[i];

		CV.warp(imageSrc, this.homography, candidate, warpSize);

		CV.threshold(
			this.homography,
			this.homography,
			CV.otsu(this.homography),
		);

		marker = this.getMarker(this.homography, candidate);
		if (marker) {
			markers.push(marker);
		}
	}

	return markers;
};

AR.Detector.prototype.getMarker = function (imageSrc, candidate) {
	var width = (imageSrc.width / 7) >>> 0,
		minZero = (width * width) >> 1,
		bits = [],
		rotations = [],
		square,
		match,
		inc,
		i,
		j;

	for (i = 0; i < 7; ++i) {
		inc = 0 === i || 6 === i ? 1 : 6;

		for (j = 0; j < 7; j += inc) {
			square = {
				x: j * width,
				y: i * width,
				width: width,
				height: width,
			};
			if (CV.countNonZero(imageSrc, square) > minZero) {
				return null;
			}
		}
	}

	for (i = 0; i < 5; ++i) {
		bits[i] = [];

		for (j = 0; j < 5; ++j) {
			square = {
				x: (j + 1) * width,
				y: (i + 1) * width,
				width: width,
				height: width,
			};

			bits[i][j] = CV.countNonZero(imageSrc, square) > minZero ? 1 : 0;
		}
	}

	rotations[0] = bits;
	rotations[1] = this.rotate(rotations[0]);
	rotations[2] = this.rotate(rotations[1]);
	rotations[3] = this.rotate(rotations[2]);

	match = this.identify(rotations);

	if (!match) {
		return null;
	}

	return new AR.Marker(match.id, this.rotate2(candidate, 4 - match.rotation));
};

/*
	Matches the 4 rotations of a scanned 5x5 bit matrix against the
	AR.DICT_5X5_250 predefined dictionary (OpenCV's DICT_5X5_250), picking
	the closest marker (by Hamming distance) within the dictionary's
	tolerated bit-error budget (maxCorrectionBits = 2).
*/
AR.Detector.prototype.identify = function (rotations) {
	var dict = AR.DICT_5X5_250,
		maxCorrectionBits = 2,
		bestId = -1,
		bestRotation = -1,
		bestDist = Infinity,
		dist,
		id,
		r;

	for (id = 0; id < dict.length; ++id) {
		for (r = 0; r < 4; ++r) {
			dist = this.bitDistance(dict[id], rotations[r]);

			if (dist < bestDist) {
				bestDist = dist;
				bestId = id;
				bestRotation = r;

				if (0 === bestDist) {
					break;
				}
			}
		}

		if (0 === bestDist) {
			break;
		}
	}

	if (bestDist <= maxCorrectionBits) {
		return { id: bestId, rotation: bestRotation };
	}

	return null;
};

AR.Detector.prototype.bitDistance = function (a, b) {
	var dist = 0,
		i,
		j;

	for (i = 0; i < 5; ++i) {
		for (j = 0; j < 5; ++j) {
			if (a[i][j] !== b[i][j]) {
				++dist;
			}
		}
	}

	return dist;
};

AR.Detector.prototype.rotate = function (src) {
	var dst = [],
		len = src.length,
		i,
		j;

	for (i = 0; i < len; ++i) {
		dst[i] = [];
		for (j = 0; j < src[i].length; ++j) {
			dst[i][j] = src[src[i].length - j - 1][i];
		}
	}

	return dst;
};

AR.Detector.prototype.rotate2 = function (src, rotation) {
	var dst = [],
		len = src.length,
		i;

	for (i = 0; i < len; ++i) {
		dst[i] = src[(rotation + i) % len];
	}

	return dst;
};
